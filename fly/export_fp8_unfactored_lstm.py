def _compile_and_get_ir_persistent(M: int, H: int, T: int) -> str:
    FH = 4 * H
    launcher    = compile_fp8_unfactored_lstm_gemm_persistent(B=M, H=H, T=T)
    h_fp8_all   = torch.zeros(T + 1, M, H, dtype=torch.uint8)
    c_inout     = torch.zeros(M, H, dtype=torch.float32)
    scale_hh    = torch.zeros(M, dtype=torch.float32)
    w_fused     = torch.zeros(FH // 16, H // 16, 2, 16, 8, dtype=torch.uint8)
    scale_wf    = torch.zeros(FH, dtype=torch.float32)
    bias        = torch.zeros(FH, dtype=torch.float32)
    ih_all      = torch.zeros(T, M, H, 4, dtype=torch.float16)
    prev = os.environ.get("COMPILE_ONLY")
    os.environ["COMPILE_ONLY"] = "1"
    try:
        launcher(h_fp8_all, c_inout, scale_hh, w_fused, scale_wf, bias, ih_all, 0, M)
    finally:
        if prev is None:
            os.environ.pop("COMPILE_ONLY", None)
        else:
            os.environ["COMPILE_ONLY"] = prev
    artifacts = list(launcher._mem_cache.values())
    if not artifacts:
        raise RuntimeError("_mem_cache is empty after persistent-LSTM compilation")
    return artifacts[0]._ir_text


def _export_one_persistent(M: int, H: int, T: int, artifacts_dir: str) -> None:
    FH  = 4 * H
    cfg = _compute_config(M, H)
    name = f"rdna_fp8_unfactored_lstm_persistent_H{H}_T{T}"
    print(f"\n[persistent LSTM M={M}, H={H}, T={T}]  tile_m={cfg['tile_m']}, "
          f"tile_n_h={cfg['tile_n_h']}, threads={cfg['threads_per_block']}, "
          f"blocks={cfg['total_blocks']}")

    ir_text     = _compile_and_get_ir_persistent(M, H, T)
    kernel_name = _find_kernel_name(ir_text)
    print(f"  Kernel: {kernel_name}  IR: {len(ir_text):,} chars")

    hsaco = _extract_hsaco(ir_text, prefer_no_wave64=True)
    if hsaco[:4] != b"\x7fELF":
        raise RuntimeError(f"Expected ELF magic, got: {hsaco[:8].hex()}")

    hsaco_path  = os.path.join(artifacts_dir, f"{name}.hsaco")
    header_path = os.path.join(artifacts_dir, f"{name}.h")
    os.makedirs(artifacts_dir, exist_ok=True)
    with open(hsaco_path, "wb") as f:
        f.write(hsaco)

    header = _HEADER_PERSISTENT.format(
        kernel_name=kernel_name,
        H=H, FH=FH, T=T, FH_div16=FH // 16, H_div16=H // 16,
        tile_m=cfg["tile_m"], tile_n_h=cfg["tile_n_h"],
        grid_h=cfg["grid_h"], threads_per_block=cfg["threads_per_block"],
    )
    _write_text(header_path, header)
    print(f"  Wrote {len(hsaco):,} bytes → {hsaco_path}")
    print(f"  Wrote header → {header_path}")


def _write_text(path: str, content: str) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w") as f:
        f.write(content)


def _export_one(M: int, H: int, artifacts_dir: str) -> None:
    FH  = 4 * H
    cfg = _compute_config(M, H)
    name = f"rdna_fp8_unfactored_lstm_H{H}"
    print(f"\n[unfactored LSTM M={M}, H={H}, 4H={FH}]  tile_m={cfg['tile_m']}, "
          f"tile_n_h={cfg['tile_n_h']}, threads={cfg['threads_per_block']}, "
          f"blocks={cfg['total_blocks']}")

    ir_text     = _compile_and_get_ir(M, H)
    kernel_name = _find_kernel_name(ir_text)
    print(f"  Kernel: {kernel_name}  IR: {len(ir_text):,} chars")

    hsaco = _extract_hsaco(ir_text, prefer_no_wave64=True)
    if hsaco[:4] != b"\x7fELF":
        raise RuntimeError(f"Expected ELF magic, got: {hsaco[:8].hex()}")

    hsaco_path  = os.path.join(artifacts_dir, f"{name}.hsaco")
    header_path = os.path.join(artifacts_dir, f"{name}.h")
    os.makedirs(artifacts_dir, exist_ok=True)
    with open(hsaco_path, "wb") as f:
        f.write(hsaco)

    header = _HEADER.format(
        kernel_name=kernel_name,
        H=H, FH=FH, FH_div16=FH // 16, H_div16=H // 16,
        tile_m=cfg["tile_m"], tile_n_h=cfg["tile_n_h"],
        grid_h=cfg["grid_h"], threads_per_block=cfg["threads_per_block"],
    )
    _write_text(header_path, header)
    print(f"  Wrote {len(hsaco):,} bytes → {hsaco_path}")
    print(f"  Wrote header → {header_path}")


# ── Main ──────────────────────────────────────────────────────────────────────

if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Export the fused unfactored fp8 LSTM kernel as HSACO + C header."
    )
    parser.add_argument("--config", metavar="FILE",
                        help="TOML config file with a list of M,H configs to batch-export")
    parser.add_argument("--M", type=int, default=512, help="Batch dimension (runtime; default: 512)")
    parser.add_argument("--H", type=int, default=1024, help="Hidden dimension (default: 1024)")
    parser.add_argument("--persistent", action="store_true",
                        help="Export the persistent kernel (all T timesteps in one launch)")
    parser.add_argument("--T", type=int, default=1024,
                        help="Sequence length for persistent kernel (default: 1024)")
    args = parser.parse_args()

    artifacts_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "artifacts")

    if args.persistent:
        _export_one_persistent(args.M, args.H, args.T, artifacts_dir)
    elif args.config:
        configs = _load_configs(args.config, default_m=args.M)
        print(f"Batch export: {len(configs)} config(s) from {args.config}")
        for M, H in configs:
            _export_one(M, H, artifacts_dir)
        print(f"\nDone. {len(configs)} config(s) exported to {artifacts_dir}/")
    else:
        _export_one(args.M, args.H, artifacts_dir)
